public class GameObj {

    private String day;
    private String time;
    private String opponentCity;
    private String opponentMascot;

    public String getDay() {
        return day;
    }

    public void setDay(String day) {
        this.day = day;
    }

    public String getTime() {
        return time;
    }

    public void setTime(String time) {
        this.time = time;
    }

    public String getOpponentCity() {
        return opponentCity;
    }

    public void setOpponentCity(String opponentCity) {
        this.opponentCity = opponentCity;
    }

    public String getOpponentMascot() {
        return opponentMascot;
    }

    public void setOpponentMascot(String opponentMascot) {
        this.opponentMascot = opponentMascot;
    }
}
